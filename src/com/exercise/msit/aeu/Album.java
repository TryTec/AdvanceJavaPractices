package com.exercise.msit.aeu;

public class Album {
	private int ID;
	private String Name;
	private int ArtistID;
	
	public int GetID() {
		return ID;
	}
	public void SetID(int value) {
		ID = value;
	}
	public String GetName() {
		return Name;
	}
	public void SetName(String value) {
		Name = value;
	}
	public int GetArtistID() {
		return ArtistID;
	}
	public void SetArtistID(int value) {
		ArtistID = value;
	}
}
